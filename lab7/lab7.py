print("Using for loop:")
for i in range(1, 11):
    print(i)

print("\nUsing while loop:")
count = 1
while count <= 10:
    print(count)
    count += 1

print("\nCharacters in 'Python':")
for char in "Python":
    print(char)


print("\nEven numbers from 2 to 10:")
number = 2
while number <= 10:
    print(number)
    number += 2